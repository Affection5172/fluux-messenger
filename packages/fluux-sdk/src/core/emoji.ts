export function shortcodeToEmoji(text: string): string {
  // Check if it's a shortcode format (starts and ends with colons)
  if (!text.startsWith(':') || !text.endsWith(':')) {
    return text
  }

  // Look up in our mapping (case-insensitive)
  const normalized = text.toLowerCase()
  return EMOJI_SHORTCODES[normalized] ?? text
}

/**
 * Convert all emoji shortcodes in an array to Unicode emoji.
 *
 * @param emojis - Array of emoji strings (could be shortcodes or Unicode)
 * @returns Array with shortcodes converted to Unicode emoji
 */
export function convertShortcodes(emojis: string[]): string[] {
  return emojis.map(shortcodeToEmoji)
}
